<script>
  import { goto } from "$app/navigation";

  import Button from "$lib/components/button.svelte";
  import CitySearch from "$lib/components/forms/city-search.svelte";
  import Field from "$lib/components/forms/field.svelte";
  import { searchIcon } from "$lib/icons";
  import { getQuery } from "./_search";

  export let servicesOptions = {};
  const categoryChoices = servicesOptions.categories;

  let category;
  let subcategory;
  let cityCode;
  let cityLabel;

  let subCategoryChoices = [];

  function handleSearch() {
    const query = getQuery(category, subcategory, cityCode, cityLabel);
    goto(`recherche?${query}`);
  }

  function handleCategoryChange(cat) {
    subCategoryChoices = cat
      ? servicesOptions.subcategories.filter(({ value }) =>
          value.startsWith(cat)
        )
      : [];
    if (cat && subcategory && !subcategory.startsWith(cat)) subcategory = null;
  }
</script>

<style>
  .wrapper {
    padding: 24px 24px 16px;
    background-color: var(--col-white);
    border-radius: 0 0 var(--s8) var(--s8);
    box-shadow: var(--shadow-l);
  }

  .wrapper-top {
    padding: 24px 24px 24px;
    background-color: var(--col-magenta-dark);
    border-radius: var(--s8) var(--s8) 0 0;
    box-shadow: var(--shadow-l);
    color: var(--col-white);
  }

  form {
    display: flex;
    flex-direction: column;
    gap: var(--s16);
  }

  @media (min-width: 820px) {
    form {
      flex-direction: row;
    }
  }
</style>

<div class="wrapper-top text-center lg:text-left">
  ⚠️ Le service DORA est actuellement <a
    class="underline"
    target="_blank"
    rel="noopener"
    href="https://communaute.inclusion.beta.gouv.fr/t/mise-en-visibilite-de-loffre-dinsertion-lancement-de-dora/4090"
    >en construction</a
  >, la recherche concerne uniquement ces 3 territoires : Loire-Atlantique,
  Ardennes et La Réunion.
</div>

<div class="wrapper">
  {#if categoryChoices}
    <form on:submit|preventDefault={handleSearch}>
      <Field
        type="select"
        name="category"
        bind:value={category}
        onSelectChange={handleCategoryChange}
        placeholder="Choisissez"
        choices={categoryChoices}
        label="Thématique"
        vertical
        required />

      <Field
        type="select"
        name="subcategory"
        bind:value={subcategory}
        placeholder="Choisissez"
        choices={subCategoryChoices}
        label="Besoin(s)"
        vertical />

      <Field type="custom" label="Lieu" name="city" required vertical>
        <CitySearch
          slot="custom-input"
          name="city"
          placeholder="Ville du bénéficiaire"
          handleChange={(city) => {
            cityCode = city.properties.citycode;
            cityLabel = `${
              city.properties.label
            } (${city.properties.postcode.slice(0, 2)})`;
          }} />
      </Field>

      <Button
        type="submit"
        label="Trouver"
        icon={searchIcon}
        disabled={!category || !cityCode}
        iconOnLeft
        horizontalBottom
        small
        preventDefaultOnMouseDown />
    </form>
  {:else}
    <p class="text-gray-text">Impossible de contacter le serveur</p>
  {/if}
</div>
