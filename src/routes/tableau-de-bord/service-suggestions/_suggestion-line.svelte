<script>
  export let label;
  export let data;
  export let isList;
  export let verticalLayout;
  export let isBool;
</script>

<div
  class="flex flex-row"
  class:flex-col={verticalLayout}
  class:hidden={isList ? !data.length : !data}
>
  <div class="font-bold mr-s16 w-1/3 text-gray-text">{label}</div>
  <div class="text-gray-text">
    {#if isList}
      {#each data as item}
        <li>{item}</li>
      {/each}
    {:else}
      <slot>
        {#if isBool}
          {data ? "Oui" : "Non"}
        {:else}
          {data}
        {/if}
      </slot>
    {/if}
  </div>
</div>
